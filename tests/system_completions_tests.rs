//! Comprehensive system tests for `bivvy completions`.
//!
//! Tests shell completion generation for all supported shells,
//! shell-specific syntax validation, subcommand coverage, help output,
//! and error handling for invalid inputs. All tests spawn via
//! `spawn_bivvy_global`, which pins `HOME` and the `XDG_*` base-directory
//! variables at a shared process-lifetime tempdir so the global bivvy
//! store stays isolated from the user environment on both macOS and
//! Linux. Each test verifies process exit codes, and help/error output
//! is captured via `insta` snapshots so regressions in user-facing text
//! are caught.
#![cfg(unix)]

mod system;

use system::helpers::*;

// =====================================================================
// HAPPY PATH — Shell-specific syntax validation
// =====================================================================
//
// Each shell has idiomatic tokens that clap_complete must emit. We assert
// on those tokens directly (rather than snapshotting the full script)
// because the scripts are large and regenerated by clap_complete — a
// snapshot would break on every clap upgrade and add no signal. The
// version-stable signal is "did clap_complete emit a real script for the
// right shell," which these tokens cover.

/// Bash completions contain the `_bivvy` function and the `COMPREPLY`
/// bash-specific builtin.
#[test]
fn completions_bash() {
    let mut s = spawn_bivvy_global(&["completions", "bash"]);
    let text = read_to_eof(&mut s);

    assert!(
        text.contains("_bivvy"),
        "Bash completions should contain '_bivvy' function name, got: {}",
        &text[..text.len().min(200)]
    );
    assert!(
        text.contains("COMPREPLY"),
        "Bash completions should contain 'COMPREPLY' bash-specific keyword, got: {}",
        &text[..text.len().min(200)]
    );
    assert_exit_code(&s, 0);
}

