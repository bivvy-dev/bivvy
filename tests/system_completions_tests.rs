// =====================================================================
// HELP
// =====================================================================

/// `--help` shows the expected description, the list of possible shell
/// values, and exits 0. The full help is snapshotted so that flag
/// renames, description edits, and shell-list changes are caught as
/// regressions.
#[test]
fn completions_help() {
    let mut s = spawn_bivvy_global(&["completions", "--help"]);
    let text = read_to_eof(&mut s);

    assert!(
        text.contains("Generate shell completions"),
        "Help should contain 'Generate shell completions', got: {}",
        &text[..text.len().min(300)]
    );
    assert!(
        text.contains("[possible values: bash, elvish, fish, powershell, zsh]"),
        "Help should list possible shell values, got: {}",
        &text[..text.len().min(500)]
    );

    insta::assert_snapshot!("completions_help", text);
    assert_exit_code(&s, 0);
}

// =====================================================================
// SAD PATH
// =====================================================================

/// Invoking `completions` with no shell argument shows the clap error
/// about a missing required argument, names `<SHELL>` as the missing
/// argument, and exits with clap's usage error code (2).
#[test]
fn completions_no_argument() {
    let mut s = spawn_bivvy_global(&["completions"]);
    let text = read_to_eof(&mut s);

    assert!(
        text.contains("the following required arguments were not provided"),
        "Missing shell arg should say 'the following required arguments were not provided', got: {}",
        &text[..text.len().min(300)]
    );
    assert!(
        text.contains("<SHELL>"),
        "Missing shell arg error should name '<SHELL>' as the missing argument, got: {}",
        &text[..text.len().min(300)]
    );

    insta::assert_snapshot!("completions_no_argument", text);
    assert_exit_code(&s, 2);
}

/// An unknown shell name shows the clap "invalid value" error, lists
/// the valid shells, and exits with clap's usage error code (2).
#[test]
fn completions_unknown_shell() {
    let mut s = spawn_bivvy_global(&["completions", "fakeshell"]);
    let text = read_to_eof(&mut s);

    assert!(
        text.contains("invalid value 'fakeshell' for '<SHELL>'"),
        "Unknown shell should say \"invalid value 'fakeshell' for '<SHELL>'\", got: {}",
        &text[..text.len().min(300)]
    );
    assert!(
        text.contains("[possible values: bash, elvish, fish, powershell, zsh]"),
        "Unknown shell error should list valid shells, got: {}",
        &text[..text.len().min(500)]
    );

    insta::assert_snapshot!("completions_unknown_shell", text);
    assert_exit_code(&s, 2);
}
